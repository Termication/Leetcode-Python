/**
 * @param {Function} fn
 * @param {Array} args
 * @param {number} t
 * @return {Function}
 */
var cancellable = function(fn, args, t) {
    let intervalId;
    let startTime = 0;
    let resultLog = [];
    
    // Function to execute fn and log the result with timestamp
    function executeFn() {
        const result = fn(...args);
        resultLog.push({ time: startTime, returned: result });
        startTime += t;
    }

    // Call the function immediately
    executeFn();

    // Set the interval to execute the function every t milliseconds
    intervalId = setInterval(executeFn, t);

    // Return the cancel function
    return function cancelFn() {
        clearInterval(intervalId);
        console.log('Cancelled');
    };
}
